def getUID():

	# Set duplicate
	duplicate = True

	# Get UID
	while duplicate:

		# Random UID
		uid = ''.join(random.choice(string.ascii_letters + string.digits) for _ in range(9))
		uid = 'FF'+uid

		# Check if exists
		duplicate = len(glob.glob('static/uploads/reads/{}'.format(uid)))

	# Return
	return uid

	
